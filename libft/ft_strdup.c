#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	j;
	unsigned int	i;
	char			*dest;

	i = 0;
	j = ft_strlen(s1);
	dest = (char *)malloc(sizeof(*dest) * (j + 1));
	if (!dest)
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
